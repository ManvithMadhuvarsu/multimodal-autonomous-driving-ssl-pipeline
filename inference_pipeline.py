    # Final summary banner
    topN = label_counts.most_common(5)
    det_summary = "  ·  ".join(f"{k}: {v}" for k, v in topN) if topN else "none"
    print(f"\n{'═'*68}")
    print(f"  ✅  PIPELINE COMPLETE")
    print(f"{'─'*68}")
    print(f"  Timestamps  : {n_timestamps}")
    print(f"  Images      : {total_images}  ({total_images} processed)")
