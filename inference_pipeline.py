class ModelRegistry:
    def __init__(self, device: str = "cpu", cache_dir: Optional[str] = None):
        self.device = device; self.cache_dir = cache_dir
        self._det = None; self._seg = None; self._seg_proc = None
        self._depth = None; self._depth_proc = None
        self._raft = None; self._raft_trans = None

    def detection(self):
        if self._det is None:
            from ultralytics import YOLO
            print("[Model 1/5] Loading YOLO11x-seg …")
            self._det = YOLO("yolo11x-seg.pt")
        return self._det

    def semantic_seg(self):
        if self._seg is None:
            from transformers import SegformerImageProcessor, SegformerForSemanticSegmentation
            mid = "nvidia/segformer-b5-finetuned-ade-640-640"
            print("[Model 2/5] Loading SegFormer-B5 …")
            self._seg_proc = SegformerImageProcessor.from_pretrained(mid, cache_dir=self.cache_dir)
            self._seg = SegformerForSemanticSegmentation.from_pretrained(
                mid, cache_dir=self.cache_dir).to(self.device).eval()
        return self._seg_proc, self._seg

    def depth(self):
        if self._depth is None:
            from transformers import AutoImageProcessor, AutoModelForDepthEstimation
            mid = "depth-anything/Depth-Anything-V2-Large-hf"
            print("[Model 3/5] Loading Depth-Anything-V2-Large …")
            self._depth_proc = AutoImageProcessor.from_pretrained(mid, cache_dir=self.cache_dir)
            self._depth = AutoModelForDepthEstimation.from_pretrained(
                mid, cache_dir=self.cache_dir).to(self.device).eval()
        return self._depth_proc, self._depth

    def optical_flow(self):
        if self._raft is None:
            from torchvision.models.optical_flow import raft_large, Raft_Large_Weights
            print("[Model 4/5] Loading RAFT-Large …")
            w = Raft_Large_Weights.DEFAULT
            self._raft = raft_large(weights=w, progress=True).to(self.device).eval()
            self._raft_trans = w.transforms()
        return self._raft, self._raft_trans


