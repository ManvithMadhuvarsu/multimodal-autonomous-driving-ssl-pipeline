# Utility — optical flow colour wheel
# ════════════════════════════════════════════════════════════════════════════
def _make_flow_colorwheel() -> np.ndarray:
    RY, YG, GC, CB, BM, MR = 15, 6, 4, 11, 13, 6
    ncols = RY + YG + GC + CB + BM + MR
    cw = np.zeros((ncols, 3), dtype=np.float32)
    col = 0
    cw[col:col+RY, 0] = 255; cw[col:col+RY, 1] = np.linspace(0, 255, RY, endpoint=False); col += RY
    cw[col:col+YG, 0] = np.linspace(255, 0, YG, endpoint=False); cw[col:col+YG, 1] = 255; col += YG
    cw[col:col+GC, 1] = 255; cw[col:col+GC, 2] = np.linspace(0, 255, GC, endpoint=False); col += GC
    cw[col:col+CB, 1] = np.linspace(255, 0, CB, endpoint=False); cw[col:col+CB, 2] = 255; col += CB
    cw[col:col+BM, 2] = 255; cw[col:col+BM, 0] = np.linspace(0, 255, BM, endpoint=False); col += BM
    cw[col:col+MR, 2] = np.linspace(255, 0, MR, endpoint=False); cw[col:col+MR, 0] = 255
    return cw

FLOW_COLORWHEEL = _make_flow_colorwheel()

def flow_to_rgb(flow: np.ndarray) -> np.ndarray:
    h, w = flow.shape[:2]
    u, v = flow[..., 0], flow[..., 1]
    rad = np.sqrt(u**2 + v**2); rad_max = rad.max() + 1e-5
    u, v, rad = u/rad_max, v/rad_max, rad/rad_max
    angle = np.arctan2(-v, -u) / np.pi
    fk = (angle + 1) / 2 * (len(FLOW_COLORWHEEL) - 1)
    k0 = np.floor(fk).astype(int) % len(FLOW_COLORWHEEL)
    k1 = (k0 + 1) % len(FLOW_COLORWHEEL)
    f  = fk - np.floor(fk)
    img = np.zeros((h, w, 3), dtype=np.float32)
    for i in range(3):
        c = (1-f)*FLOW_COLORWHEEL[k0,i]/255. + f*FLOW_COLORWHEEL[k1,i]/255.
        c = 1 - rad[..., None] * (1 - c[..., None])
        img[..., i] = c.squeeze()
    return (img * 255).clip(0, 255).astype(np.uint8)


