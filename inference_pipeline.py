# 6-camera collage compositor
# ════════════════════════════════════════════════════════════════════════════

def build_6cam_collage(cam_images: dict[str, np.ndarray],
                        modality_label: str,
                        timestamp: int,
                        total: int,
                        cell_w: int = 640,
                        cell_h: int = 360) -> np.ndarray:
    """
    Compose a 3×2 collage from 6 camera views for a single modality.

    Layout:
        [CAM_FRONT_LEFT]   [CAM_FRONT]   [CAM_FRONT_RIGHT]
        [CAM_BACK_LEFT ]   [CAM_BACK ]   [CAM_BACK_RIGHT ]

    cam_images : dict mapping cam name → BGR numpy image
    modality_label : e.g. "RGB Input", "SegFormer-B5 Semantic Seg"
    Returns : BGR numpy collage
    """
    HEADER_H  = 32
    TITLE_H   = 48
    FOOTER_H  = 36
    BORDER    = 2
    COLS      = 3

    # Accent colours per cam position (top row = warm, bottom row = cool)
    cam_accents = {
        "CAM_FRONT_LEFT":  (60,  200, 255),
        "CAM_FRONT":       (0,   240, 120),
        "CAM_FRONT_RIGHT": (60,  200, 255),
        "CAM_BACK_LEFT":   (200, 120, 255),
        "CAM_BACK":        (255, 150, 60),
        "CAM_BACK_RIGHT":  (200, 120, 255),
    }

    rows = []
    for row_i in range(2):
        row_cells = []
        for col_i in range(COLS):
            cam = CAM_ORDER[row_i * COLS + col_i]
            img = cam_images.get(cam)
            if img is None:
                img = np.zeros((cell_h, cell_w, 3), dtype=np.uint8)
                cv2.putText(img, f"{cam} — N/A", (20, cell_h//2),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.6, (80,80,80), 1)
            else:
                img = cv2.resize(img, (cell_w, cell_h), interpolation=cv2.INTER_LINEAR)

            # Per-camera label strip
            header = np.full((HEADER_H, cell_w, 3), (12, 14, 18), dtype=np.uint8)
            accent = cam_accents[cam]
            cv2.putText(header, CAM_LABELS[cam], (10, HEADER_H-8),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.55, accent, 1, cv2.LINE_AA)

            # Border
            img_b = cv2.copyMakeBorder(img, BORDER, BORDER, BORDER, BORDER,
                                        cv2.BORDER_CONSTANT, value=(40,40,50))
            img_b = cv2.resize(img_b, (cell_w, cell_h))
            row_cells.append(np.vstack([header, img_b]))
        rows.append(np.hstack(row_cells))

    grid = np.vstack(rows)
    panel_w = grid.shape[1]

    # Title bar
    title = np.full((TITLE_H, panel_w, 3), (8, 8, 12), dtype=np.uint8)
    cv2.putText(title, f"{modality_label}  —  6-Camera 360° View",
                (16, 32), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 200, 255), 1, cv2.LINE_AA)

    # Footer
    footer = np.full((FOOTER_H, panel_w, 3), (10, 10, 14), dtype=np.uint8)
    cv2.putText(footer, f"Timestamp {timestamp+1}/{total}",
                (16, 24), cv2.FONT_HERSHEY_SIMPLEX, 0.52, (100, 180, 255), 1, cv2.LINE_AA)
    cv2.putText(footer, "QuadFusion-AD  |  nuScenes v1.0-mini",
                (panel_w - 420, 24), cv2.FONT_HERSHEY_SIMPLEX, 0.46, (140, 140, 140), 1)

    return np.vstack([title, grid, footer])


