# GIF creator
# ════════════════════════════════════════════════════════════════════════════
def save_gif(frame_paths: list[Path], out_path: Path,
             fps: int = 4, max_width: int = 1280) -> None:
    frames_pil = []
    for p in frame_paths:
        img = cv2.imread(str(p))
        if img is None: continue
        h,w = img.shape[:2]
        if w > max_width:
            img = cv2.resize(img,(max_width,int(h*max_width/w)),interpolation=cv2.INTER_AREA)
        frames_pil.append(Image.fromarray(cv2.cvtColor(img,cv2.COLOR_BGR2RGB))
                          .convert("P",palette=Image.ADAPTIVE,colors=256))
    if not frames_pil: print("  [GIF] No frames."); return
    frames_pil[0].save(str(out_path),save_all=True,append_images=frames_pil[1:],
                       duration=int(1000/fps),loop=0,optimize=True)
    print(f"  [GIF] → {out_path}  ({len(frames_pil)} frames, {out_path.stat().st_size//1024} KB, {fps} fps)")


def collect_images(input_path: str) -> list[Path]:
    p = Path(input_path)
    if p.is_file():
        if p.suffix.lower() in IMG_EXTS: return [p]
        raise ValueError(f"Unsupported: {p}")
    if p.is_dir():
        imgs = sorted(f for f in p.iterdir() if f.suffix.lower() in IMG_EXTS)
        if not imgs: raise ValueError(f"No images in: {p}")
        return imgs
    raise ValueError(f"Path not found: {p}")


