def depth_to_thermal(depth_map: np.ndarray, rgb: np.ndarray) -> np.ndarray:
    d = depth_map.astype(np.float32)
    d_norm = 1.0 - (d - d.min()) / (d.max() - d.min() + 1e-5)
    gray   = cv2.cvtColor(rgb, cv2.COLOR_BGR2GRAY).astype(np.float32) / 255.
    edges  = np.abs(cv2.Laplacian(gray, cv2.CV_32F)); edges /= (edges.max() + 1e-5)
    t = (0.7 * d_norm + 0.3 * edges)
    return cv2.applyColorMap((t * 255).clip(0, 255).astype(np.uint8), cv2.COLORMAP_INFERNO)


def _depth_to_pseudo_lidar(depth: np.ndarray, fx: float = 500.0,
                            fy: float = 500.0) -> np.ndarray:
    h, w = depth.shape; cx, cy = w/2, h/2
    u = np.arange(w, dtype=np.float32); v = np.arange(h, dtype=np.float32)
    uu, vv = np.meshgrid(u, v)
    z = depth.astype(np.float32)
    x = (uu - cx) * z / fx; y = (vv - cy) * z / fy
    pts = np.stack([x, y, z], axis=-1).reshape(-1, 3)
    mask = (z.reshape(-1) > 0.02) & (z.reshape(-1) < 0.97)
    return pts[mask]


def render_bev_canvas(pts: np.ndarray, boxes_bev: list,
                       canvas_size: int = 360, max_range: float = 50.0) -> np.ndarray:
    canvas = np.zeros((canvas_size, canvas_size, 3), dtype=np.uint8); canvas[:] = (12, 14, 18)
    for r in [0.2, 0.4, 0.6, 0.8]:
        cv2.circle(canvas, (canvas_size//2, canvas_size//2),
                   int(r * canvas_size/2), (30, 35, 40), 1)
    def _w2p(x, z):
        return (int(canvas_size/2 + x/max_range*canvas_size/2),
                int(canvas_size   - z/max_range*canvas_size))
    if len(pts) > 0:
        step = max(1, len(pts) // 4000)
        for p in pts[::step]:
            px, py = _w2p(p[0], p[2])
            if 0 <= px < canvas_size and 0 <= py < canvas_size:
                intensity = min(255, int(180 + p[1] * 80))
                canvas[py, px] = (0, intensity//3, intensity)
    ex, ez = canvas_size//2, canvas_size-20
    cv2.rectangle(canvas, (ex-6, ez-10), (ex+6, ez+4), (0, 220, 120), -1)
    cv2.putText(canvas, "EGO", (ex-12, ez+18), cv2.FONT_HERSHEY_SIMPLEX, 0.3, (0,220,120), 1)
    for box in boxes_bev:
        cx2, cz2 = box["cx"], box["cz"]; hw, hl = box["w"]/2, box["l"]/2
        col = box.get("colour", (0, 200, 255))
        poly = np.array([_w2p(cx2-hw,cz2-hl),_w2p(cx2+hw,cz2-hl),
                         _w2p(cx2+hw,cz2+hl),_w2p(cx2-hw,cz2+hl)], dtype=np.int32)
        cv2.polylines(canvas, [poly], True, col, 2)
        lx, lz = _w2p(cx2, cz2)
        cv2.putText(canvas, box["label"][:3], (lx-8, lz-4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.3, col, 1)
    return canvas


def _detections_to_bev_boxes(detections: list, depth: np.ndarray,
                              h: int, w: int, fx: float = 500.0) -> list:
    boxes_bev = []
    for det in detections:
        x1, y1, x2, y2 = det["bbox"]
        x1c,x2c = max(0,x1), min(w-1,x2); y1c,y2c = max(0,y1), min(h-1,y2)
        if x2c<=x1c or y2c<=y1c: continue
        roi = depth[y1c:y2c, x1c:x2c]
        if roi.size == 0: continue
        z = float(np.median(roi)); z_m = z * 50.0
        cx2 = ((x1+x2)/2 - w/2) * z_m / fx
        bw = max(0.5, (x2-x1)*z_m/fx); bl = max(0.5, bw*1.4)
        boxes_bev.append({"label": det["label"], "cx": cx2, "cz": z_m,
                          "w": bw, "l": bl,
                          "colour": NUSCENES_PALETTE.get(det["label"], (180,180,60))})
    return boxes_bev


def simulate_radar_fusion(depth: np.ndarray, flow_bgr: np.ndarray,
                           detections: list, bgr: np.ndarray) -> np.ndarray:
    h, w = depth.shape
    rd = np.zeros((h, w, 3), dtype=np.uint8); rd[:] = (8, 10, 14)
    d_norm = (depth - depth.min()) / (depth.max() - depth.min() + 1e-5)
    dist_col = cv2.applyColorMap(((1-d_norm)*200).clip(0,255).astype(np.uint8), cv2.COLORMAP_OCEAN)
    rd = cv2.addWeighted(rd, 0.3, dist_col, 0.7, 0)
    flow_gray = cv2.cvtColor(flow_bgr, cv2.COLOR_BGR2GRAY).astype(np.float32) / 255.
    flow_mag  = cv2.GaussianBlur(flow_gray, (15,15), 0)
    vel_col   = cv2.applyColorMap((flow_mag*255).clip(0,255).astype(np.uint8), cv2.COLORMAP_HOT)
    rd = (rd * (1-flow_mag[...,None]) + vel_col * flow_mag[...,None]).clip(0,255).astype(np.uint8)
    for det in detections:
        x1,y1,x2,y2 = det["bbox"]; cx2,cy2 = (x1+x2)//2, (y1+y2)//2
        x1c,x2c = max(0,x1),min(w-1,x2); y1c,y2c = max(0,y1),min(h-1,y2)
        label = det["label"]; colour = NUSCENES_PALETTE.get(label, (200,200,60))
        roi_d = depth[y1c:y2c,x1c:x2c]; range_m = float(np.median(roi_d))*50.0 if roi_d.size else 0
        fy_roi = flow_gray[y1c:y2c,x1c:x2c]; vel_ms = float(np.median(fy_roi))*20.0 if fy_roi.size else 0
        cv2.ellipse(rd, (cx2,cy2), (max(8,(x2-x1)//3), max(4,(y2-y1)//4)), 0, 0, 360, colour, 2)
        al = int(vel_ms*3)
        if al > 2: cv2.arrowedLine(rd, (cx2,cy2), (cx2,cy2-al), colour, 2, tipLength=0.4)
        cv2.putText(rd, f"{label[:4]} {range_m:.0f}m {vel_ms:.1f}m/s",
                    (x1, max(10,y1-6)), cv2.FONT_HERSHEY_SIMPLEX, 0.38, colour, 1)
    for r_frac in [0.2,0.4,0.6,0.8]:
        y_line = int(h*r_frac); r_m = (1-r_frac)*50
        cv2.line(rd,(0,y_line),(w,y_line),(30,40,50),1)
        cv2.putText(rd, f"{r_m:.0f}m", (4,y_line-3), cv2.FONT_HERSHEY_SIMPLEX, 0.3, (60,80,100), 1)
    cv2.rectangle(rd,(0,0),(260,22),(0,0,0),-1)
    cv2.putText(rd,"RadarVoxelFusionNet (TUM)",(4,15),cv2.FONT_HERSHEY_SIMPLEX,0.38,(0,200,255),1)
    return rd


