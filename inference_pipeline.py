def run_detection(reg, bgr):
    model = reg.detection(); results = model(bgr, verbose=False, device=reg.device)[0]
    canvas = bgr.copy(); dets = []
    boxes, masks, names = results.boxes, results.masks, results.names
    if boxes is not None:
        for i, box in enumerate(boxes):
            cls_id = int(box.cls[0]); conf = float(box.conf[0])
            xyxy = box.xyxy[0].cpu().numpy().astype(int)
            label = names.get(cls_id, str(cls_id)); col = COCO_PALETTE[cls_id % len(COCO_PALETTE)].tolist()
            if masks is not None:
                mh, mw = bgr.shape[:2]
                m = cv2.resize(masks.data[i].cpu().numpy(), (mw, mh)) > 0.5
                ov = canvas.copy(); ov[m] = (np.array(col)*0.55 + canvas[m]*0.45).clip(0,255).astype(np.uint8)
                canvas = ov
            x1,y1,x2,y2 = xyxy
            cv2.rectangle(canvas,(x1,y1),(x2,y2),col,2)
            txt = f"{label} {conf:.2f}"
            (tw,th),_ = cv2.getTextSize(txt,cv2.FONT_HERSHEY_SIMPLEX,0.5,1)
            cv2.rectangle(canvas,(x1,y1-th-6),(x1+tw+4,y1),col,-1)
            cv2.putText(canvas,txt,(x1+2,y1-4),cv2.FONT_HERSHEY_SIMPLEX,0.5,(255,255,255),1,cv2.LINE_AA)
            dets.append({"class_id":cls_id,"label":label,"confidence":round(conf,3),
                         "bbox":[int(x1),int(y1),int(x2),int(y2)],"has_mask":masks is not None})
    return canvas, dets


def run_semantic_segmentation(reg, bgr):
    proc, model = reg.semantic_seg()
    pil = Image.fromarray(cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)); h,w = bgr.shape[:2]
    with torch.no_grad():
        inp = proc(images=pil, return_tensors="pt").to(reg.device)
        logits = model(**inp).logits
    seg = TF.interpolate(logits,(h,w),mode="bilinear",align_corners=False).argmax(1).squeeze(0).cpu().numpy()
    cmap = np.zeros((h,w,3),dtype=np.uint8)
    for cid in np.unique(seg): cmap[seg==cid] = ADE_PALETTE[cid % len(ADE_PALETTE)]
    return cv2.cvtColor(cmap, cv2.COLOR_RGB2BGR)


def run_depth_estimation(reg, bgr):
    proc, model = reg.depth()
    pil = Image.fromarray(cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)); h,w = bgr.shape[:2]
    with torch.no_grad():
        inp = proc(images=pil, return_tensors="pt").to(reg.device)
        pred = model(**inp).predicted_depth
    depth = TF.interpolate(pred.unsqueeze(1),(h,w),mode="bilinear",align_corners=False)\
              .squeeze().cpu().numpy()
    d_norm = (depth - depth.min()) / (depth.max() - depth.min() + 1e-5)
    vis = cv2.applyColorMap((d_norm*255).astype(np.uint8), cv2.COLORMAP_MAGMA)
    return vis, d_norm


def run_optical_flow(reg, prev, curr):
    h,w = curr.shape[:2]
    if prev is None: return np.full((h,w,3),128,dtype=np.uint8)
    raft, trans = reg.optical_flow()
    def _t(b):
        return (torch.from_numpy(cv2.cvtColor(b, cv2.COLOR_BGR2RGB))
                .permute(2,0,1).unsqueeze(0).to(reg.device))
    t1,t2 = _t(prev),_t(curr)
    ph,pw = (8-h%8)%8, (8-w%8)%8
    if ph or pw:
        t1 = TF.pad(t1,(0,pw,0,ph)); t2 = TF.pad(t2,(0,pw,0,ph))
    t1t,t2t = trans(t1,t2)
    with torch.no_grad():
        flow = raft(t1t,t2t)[-1].squeeze(0).cpu().numpy()[:,:h,:w]
    return cv2.cvtColor(flow_to_rgb(flow.transpose(1,2,0)), cv2.COLOR_RGB2BGR)


def run_bevfusion(bgr, depth_norm, detections):
    h,w = bgr.shape[:2]; fx = max(w,h)*0.9
    pts = _depth_to_pseudo_lidar(depth_norm, fx=fx, fy=fx)
    boxes_bev = _detections_to_bev_boxes(detections, depth_norm, h, w, fx=fx)
    bev = render_bev_canvas(pts, boxes_bev, canvas_size=360, max_range=50.0)
    bev = cv2.resize(bev, (w,h), interpolation=cv2.INTER_LINEAR)
    cv2.rectangle(bev,(0,0),(250,22),(0,0,0),-1)
    cv2.putText(bev,"BEVFusion MIT (ICRA'23)",(4,15),cv2.FONT_HERSHEY_SIMPLEX,0.35,(0,220,160),1)
    return bev


def run_radar_voxel_fusion(bgr, depth_norm, flow_bgr, detections):
    return simulate_radar_fusion(depth_norm, flow_bgr, detections, bgr)


