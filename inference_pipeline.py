# Original 4×2 panel composer (flat / single-image mode)
# ════════════════════════════════════════════════════════════════════════════
def compose_panel(original, detection, semantic, depth_vis, flow,
                  thermal, bev, radar_fus, detections, frame_idx, total_frames):
    CELL_W, CELL_H = 640, 360; HEADER_H = 44; BORDER = 2
    cells = [
        ("RGB — Input",                         original),
        ("YOLO11x-seg — Detection + Inst.Seg",  detection),
        ("SegFormer-B5 — Semantic Seg",          semantic),
        ("Depth-Anything-V2-Large",              depth_vis),
        ("RAFT-Large — Optical Flow",            flow),
        ("Thermal Simulation — FLIR-style",      thermal),
        ("BEVFusion MIT (ICRA'23) — BEV",        bev),
        ("RadarVoxelFusionNet (TUM)",             radar_fus),
    ]
    rows = []
    for row_i in range(4):
        row_cells = []
        for col_i in range(2):
            title, img = cells[row_i*2+col_i]
            img_r = cv2.resize(img,(CELL_W,CELL_H),interpolation=cv2.INTER_LINEAR)
            header = np.full((HEADER_H,CELL_W,3),(15,15,20),dtype=np.uint8)
            accent = {0:(0,220,160),1:(0,200,255),2:(255,180,0),3:(200,80,255)}
            cv2.putText(header,title,(10,30),cv2.FONT_HERSHEY_SIMPLEX,0.58,accent[row_i],1,cv2.LINE_AA)
            img_b = cv2.copyMakeBorder(img_r,BORDER,BORDER,BORDER,BORDER,cv2.BORDER_CONSTANT,value=(40,40,50))
            img_b = cv2.resize(img_b,(CELL_W,CELL_H))
            row_cells.append(np.vstack([header,img_b]))
        rows.append(np.hstack(row_cells))
    panel = np.vstack(rows)
    footer = np.full((52,panel.shape[1],3),(10,10,14),dtype=np.uint8)
    cc = Counter(d["label"] for d in detections)
    summary = "  ·  ".join(f"{k}: {v}" for k,v in cc.most_common(5)) or "No detections"
    cv2.putText(footer,f"Frame {frame_idx+1}/{total_frames}",(12,34),cv2.FONT_HERSHEY_SIMPLEX,0.6,(100,180,255),1)
    cv2.putText(footer,summary,(200,34),cv2.FONT_HERSHEY_SIMPLEX,0.52,(200,200,200),1)
    cv2.putText(footer,f"Objects: {len(detections)}",(panel.shape[1]-200,34),cv2.FONT_HERSHEY_SIMPLEX,0.55,(80,230,140),1)
    panel = np.vstack([panel,footer])
    tbar = np.full((52,panel.shape[1],3),(8,8,12),dtype=np.uint8)
    cv2.putText(tbar,"MULTIMODAL PERCEPTION  |  YOLO11x · SegFormer · DepthV2 · RAFT · BEVFusion · RadarVoxelFusionNet",
                (16,34),cv2.FONT_HERSHEY_SIMPLEX,0.5,(0,200,255),1)
    return np.vstack([tbar,panel])


